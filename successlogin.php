  <p><b><font color="#6A0000">Since 1986, P &amp; B has created a reputation for re-interpreting and improvising on classic designs, continuously keeping them fresh and alluring. P &amp; B's signature style of combining hardwood, hand-forged brass and selected leather hides achieve a standard of detail and meticulous craftsmanship that has all disappeared in a world of increasing manufacture of ubiquitous pieces. The uniqueness of P &amp; B products lie in their ability to work with equal virtuosity in a variety of visually interactive, durable and traditional (not to mention sustainable) materials such as select hardwood, hand tipped hides, brass, rattan, steel, glass and textile.</font></b></p>
