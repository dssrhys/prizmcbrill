  <h1>&nbsp;</h1>
  <h1><font color="#6A0000">Friends &amp; Found Memories</font></h1>
  <p><font color="#6A0000"><b>Somewhere in the tropics unearthed by a mild tsunami sat this unusual Trunk that looked as if it were owned by a traveler from another time. The latch  came ajar and out spilled letters that were surprisingly well preserved. With a closer look the letters spoke of love, romance and exotic travel that really struck the imagination. There were also some very interesting looking and what seemed to be stage passes to a most unusual Festival.</b></font></p>
  <p><font color="#6A0000"><b>The trunk and letters were  from a time that all philosophers were unsuccessful in finding the truth in a world full of eccentricities. Did these artifacts belong to a curious traveler, a poet, a magician or a romantic fool? It seems this trunk had changed hands through the years with two sets of initials masked to its side.</b></font></p>
  <p><font color="#6A0000"><b>A Short passage stood out in one of the letters… it read…</b></font></p>
  <p><font color="#6A0000"><b>&quot;Time is that elastic band you are holding in your hands. You are stretching it. More and more. And it feels so elastic, so endlessly elastic that you can pull it all the way to the farthest horizon… and beyond. You feel you could touch the sun… and maybe even further… and when you’re completely convinced that this is so, it ruptures and stings your fingers. Maybe that is the pain of time… the time which lasts and will always manifest in different ways.  But…&quot;   (paper torn)</b></font></p>
  <p><font color="#6A0000"><b>It seems this trunk stood through the test of time for both its owners. If only this mysterious wardrobe could talk.</b></font></p>
  <p><font color="#6A0000"><b>The thought of being the new owner of this valise was exciting . Can you imagine being the care taker of endless memories. A patron of the arts once said, &quot;richness of life lies in memories we have forgotten&quot;.</b></font></p>
  <p><font color="#6A0000"><b>Let’s try to remember.</b></font></p>
